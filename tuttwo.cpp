#include<iostream>
using namespace std;
int main()
{
    int a=6;
    int b=7;
    cout<<"types of operator in cpp";
  
    cout<<"the value of a+b\t"<<a+b<<endl;
    cout<<"the value of a-b\t"<<a-b<<endl;
    cout<<"the value of a*b\t"<<a*b<<endl;
    cout<<"the value of a/b\t"<<a/b<<endl;
    cout<<"the value of a%b\t"<<a%b<<endl;
    cout<<"the value of a++\t"<<a++<<endl;
    cout<<"the value of a--\t"<<a--<<endl;
    cout<<"the value of ++b\t"<<++b<<endl;
    cout<<"the value of --b\t"<<--b<<endl;

    return 0;

}